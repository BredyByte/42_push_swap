#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*cur;

	cur = (char *)s + ft_strlen(s, 'c');
	while (*cur != (char)c)
		if (cur-- == s)
			return (NULL);
	return (cur);
}
