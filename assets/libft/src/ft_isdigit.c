#include "libft.h"

int	ft_isdigit(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (c >= '0' && c <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}
